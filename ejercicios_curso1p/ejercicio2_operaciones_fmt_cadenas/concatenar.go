package main

import "fmt"

func main(){

	// imprimir cadena golang concatenada
	fmt.Println("go"+"lang")

	// sumar numeros enteros
	fmt.Println("5+5 = ", 5+5)

	// manejar flotantes
	fmt.Println("7.5/3.2 = ", 7.5/3.2)
	fmt.Println("7.0/3.0 = ", 7.0/3.0) // forma obligatoria

	// manejo booleanos
	fmt.Println("valores =")
	fmt.Println(true&&false)
	fmt.Println(true||false)
	fmt.Println(!false)


}