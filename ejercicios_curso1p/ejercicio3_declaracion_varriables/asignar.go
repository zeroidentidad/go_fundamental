package main

import "fmt"

func main(){
	// declaracion string

	var a string = "texto inicial"
	fmt.Println(a)

	// declarando conjunto variales
	var b, c, h int = 4, 5, 0
	fmt.Println(b,c, h)

	// declaracion con valores booleanos, ejemplo instanciacion
	var d = true
	fmt.Println(d)

	// declaracion tipo entero
	var e int
	fmt.Println(e)

	// declaracion y asingacion con otra sintaxis
	f:= "culito"
	fmt.Println(f)

}